palette_t current_palette = PALETTE_PLASMA;

uint16_t palette_img_buf[PALETTE_WIDTH * PALETTE_HEIGHT];


void palette_generate(void)
{
  const uint16_t* cmap = get_current_palette(); // 현재 사용 팔레트
  int idx;

  for (int y = 0; y < PALETTE_HEIGHT; y++)
  {
    idx = 255 - y; // 아래→위로 증가시키기 위해 반전
    uint16_t color = cmap[idx];

    for (int x = 0; x < PALETTE_WIDTH; x++)
    {
      palette_img_buf[y * PALETTE_WIDTH + x] = color;
    }
  }
}

void *colorbarGetFrameBuffer(void)
{
  return (void *)palette_img_buf;
}


